function show_main() {
    var intro = document.getElementById("intro");
    var main = document.getElementById("main")
    intro.style.visibility = "hidden"
    main.style.visibility = "visible"
}
function show_intro() {
    var intro = document.getElementById("intro");
    var main = document.getElementById("main")
    intro.style.visibility = "visible"
    main.style.visibility = "hidden"
}